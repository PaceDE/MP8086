;Program to print string in reverse

.MODEL SMALL

.DATA
STRING DB "MICROPROCESSOR",13,10,"$"
.CODE
MAIN PROC
    
.STARTUP  
     
LEA DX,STRING
MOV AH,9
INT 21H

    

MOV CX, 14; LENGTH OF STRING
MOV SI,OFFSET STRING
ADD SI,13
REPEAT:  

MOV DL,[SI]
MOV AH,02H
INT 21H
DEC SI
LOOP REPEAT 

.EXIT
MAIN ENDP
END MAIN